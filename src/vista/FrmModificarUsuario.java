    private void btn_modificar1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_modificar1ActionPerformed
         try {
           String ID = txt_idUsuario.getText().trim();
           Connection cn = DriverManager.getConnection("jdbc:mysql://localhost/bd_punto_de_ventas", "root", "");
           PreparedStatement pst = cn.prepareStatement("update usuarios set usuario = ?, contrasenia = ? where ID = " + ID);
           pst.setString(1, (txt_nombre.getText().trim()));
           pst.setString(2, (txt_contrasenia.getText().trim()));
           pst.executeUpdate();
           lb_pantallaConfirmacion.setText("Modificaci√≥n exitosa!");
        } catch(SQLException e){
            JOptionPane.showMessageDialog(null, "No se pudo tener acceso a la base de datos");
        }catch (Exception e) {
             lb_pantallaConfirmacion.setText("Alg√∫n campo no es v√°lido!");
        }
    }//GEN-LAST:event_btn_modificar1ActionPerformed

    private void btn_buscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_buscarActionPerformed
        try {
            Connection cn = DriverManager.getConnection("jdbc:mysql://localhost/bd_punto_de_ventas", "root", "");
            PreparedStatement pst = cn.prepareStatement("select * from usuarios where ID = ?");

            pst.setString(1, this.txt_idUsuario.getText().trim());

            ResultSet rs = pst.executeQuery();
            if (rs.next()) {
                
                txt_nombre.setText(rs.getString("usuario"));
                txt_contrasenia.setText(rs.getString("contrasenia"));
            } else {
                lb_pantallaConfirmacion.setText("Datos del producto no encontrados");
            }

        } catch(SQLException e){
            JOptionPane.showMessageDialog(null, "No se pudo tener acceso a la base de datos");
        }catch (Exception e) {
             lb_pantallaConfirmacion.setText("Entrada de ID np v√°lida!");
        }
    }//GEN-LAST:event_btn_buscarActionPerformed

