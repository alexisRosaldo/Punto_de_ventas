    private void btn_eliminarProductoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_eliminarProductoActionPerformed
        // TODO add your handling code here:
        
         try {
            Connection cn = DriverManager.getConnection("jdbc:mysql://localhost/bd_punto_de_ventas", "root", "");
            PreparedStatement pst = cn.prepareStatement("delete from productos where ID = ?");
            
            pst.setString(1, txt_idProducto.getText().trim());

            pst.executeUpdate();
            this.txt_idProducto.setText("");
            lb_pantallaCarga.setText("Eliminacion del producto exitosa!!!");
                
        }catch(SQLException e){
            JOptionPane.showMessageDialog(null, "No se pudo tener acceso a la base de datos");
        }catch (Exception e) {
            JOptionPane.showMessageDialog(null, "El ID del producto es incorrecto");
        }
        
    }//GEN-LAST:event_btn_eliminarProductoActionPerformed

