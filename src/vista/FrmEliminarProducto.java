    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            Connection cn = DriverManager.getConnection("jdbc:mysql://localhost/bd_punto_de_ventas", "root", "");
            PreparedStatement pst = cn.prepareStatement("select * from productos where ID = ?");

            pst.setString(1, this.txt_idProducto.getText().trim());

            ResultSet rs = pst.executeQuery();
            if (rs.next()) {
                lb_datosEncontrados.setText("ID:  " + rs.getString("ID") + "      Nombre:  " + rs.getString("NombreProducto"));
            } else {
                lb_datosEncontrados.setText("Producto no encontrado!");
            }
        } catch(SQLException e){
            JOptionPane.showMessageDialog(null, "No se pudo tener acceso a la base de datos");
        } catch (Exception e) {
            lb_datosEncontrados.setText("Entrada de ID no valida");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

