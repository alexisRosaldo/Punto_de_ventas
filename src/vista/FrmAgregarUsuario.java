    private void btn_crearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_crearActionPerformed
         try {
            Connection cn = DriverManager.getConnection("jdbc:mysql://localhost/bd_punto_de_ventas", "root", "");
            PreparedStatement pst = cn.prepareStatement("insert into usuarios values(?,?,?)");
                         
            pst.setString(1, "0");
            pst.setString(2, txt_nombre.getText().trim());
            pst.setString(3, password_contrasenia.getText().trim());
            pst.executeUpdate();
            
            txt_nombre.setText("");
            password_contrasenia.setText("");
            lb_confirmacion.setText("Registro exitoso!!!"); 
        }catch(SQLException e){
            JOptionPane.showMessageDialog(null, "No se pudo tener acceso a la base de datos");
        }catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Existe algun/algunos campos invalidos");
        }
    }//GEN-LAST:event_btn_crearActionPerformed
    
